#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char	i;

	i = 0;
	while (i[str] != '\0' )
	{
		ft_putchar(i[str]);
		i++;
	}
}
